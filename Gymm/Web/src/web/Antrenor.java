    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        String TCNO = tc_alani.getText();
        String ad = ad_alani.getText();
        String soyad = soyad_alani.getText();
        String TelefonNo = telefon_alani.getText();
        String Dogumtarihi = tarih_alani.getText();
        String Password = sifre_alani.getText();
        String Tip = brans_alani.getText();
        String Sube = sube_alani.getText();
        
        
        int selectedrow = antrenor_tablo.getSelectedRow();
        
        if(selectedrow == -1){
            
            if(model.getRowCount() == 0){
                mesaj_alani.setText("Tablo boş.");
            }
            else{
                mesaj_alani.setText("Antrenör seçin!");
            }
        }
        else{
            int id = (int)model.getValueAt(selectedrow, 0);
            
            islemler.antrenorGuncelle(id,TCNO,ad,soyad,TelefonNo,Dogumtarihi,Password);
            
            antrenorGoruntule();
            
            mesaj_alani.setText("Başarıyla güncellendi.");
        }
    }//GEN-LAST:event_guncelleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        mesaj_alani.setText("");
        
        int selectedrow = antrenor_tablo.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                mesaj_alani.setText("Tablo boş.");
            }
            else{
                mesaj_alani.setText("Antrenör seçin!");
            }
        }
        else{
            int id = (int)model.getValueAt(selectedrow,0);
            
            islemler.antrenorSil(id);
            
            antrenorGoruntule();
            
            mesaj_alani.setText("Başarıyla silindi.");
        }
    }//GEN-LAST:event_silActionPerformed

