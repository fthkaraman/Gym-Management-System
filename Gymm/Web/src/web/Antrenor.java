    /**
     * @param args the command line arguments
     */
    public void antrenorGoruntule(){
        model.setRowCount(0); 
        
        ArrayList<AntrenorBilgi> antrenorler = new ArrayList<AntrenorBilgi>();
        
        antrenorler = islemler.antrenorGoster();
        
        if(antrenorler != null){
            for(AntrenorBilgi antrenor : antrenorler){
                Object[] eklenecek = {antrenor.getId(),antrenor.getTCNO(),antrenor.getAd(),antrenor.getSoyad(),antrenor.getTelefonNo(),antrenor.getDogumtarihi(),antrenor.getPassword(),antrenor.getTip(),antrenor.getSube()};
                model.addRow(eklenecek);
            }
        }
    }
