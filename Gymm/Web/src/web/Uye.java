    public void uyeGoruntule(){
        model.setRowCount(0); 
        
        ArrayList<UyeBilgi> uyeler = new ArrayList<UyeBilgi>();
        
        uyeler = islemler.uyeGoster();
        
        if(uyeler != null){
            for(UyeBilgi uye : uyeler){
                Object[] eklenecek = {uye.getUyeID(),uye.getTCNO(),uye.getAd(),uye.getSoyad(),uye.getTelefonNo(),uye.getAdres(),uye.getEmail(),uye.getCinsiyet(),uye.getDogumtarihi(),uye.getIndeks(),uye.getTip(),uye.getUyelik(),uye.getAntrenor()};
                model.addRow(eklenecek);
            }
        }
    }
    
