    private void uye_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uye_silActionPerformed
        
        mesaj_alani.setText("");
        
        int selectedrow = uye_tablo.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() == 0){
                mesaj_alani.setText("Tablo boş.");
            }
            else{
                mesaj_alani.setText("Üye seçin!");
            }
        }
        else{
            int id = (int)model.getValueAt(selectedrow,0);
            
            islemler.uyeSil(id);
            
            uyeGoruntule();
            
            mesaj_alani.setText("Başarıyla silindi.");
        }
    }//GEN-LAST:event_uye_silActionPerformed

